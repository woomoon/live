package com.woomoon.entitys;

public class LiveEntity {

	private String live_num;
	private String pat_num;
	private String live_count;

	public String getLive_num() {
		return live_num;
	}
	public String getPat_num() {
		return pat_num;
	}
	public String getLive_count() {
		return live_count;
	}
	public void setLive_num(String live_num) {
		this.live_num = live_num;
	}
	public void setPat_num(String pat_num) {
		this.pat_num = pat_num;
	}
	public void setLive_count(String live_count) {
		this.live_count = live_count;
	}


}